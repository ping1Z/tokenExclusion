public class MEMsg {
    private int id;
    private String host;
    private int port;
    private String content;

    public void setId(int id){
        this.id = id;
    }
    public void setHost(String host){
        this.host = host;
    }
    public void setPort(int port){
        this.port = port;
    }
    public void setContent(String content){
        this.content = content;
    }
}
